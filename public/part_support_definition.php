<b>fut &#x2022 sal</b> | <i>ˈfo͝otsäl</i>|,<br />
noun [ mass noun ] <br />
a modified form of soccer played with five players per side on a smaller, typically indoor, field. ORIGIN 1980s: partly from Brazilian Portuguese, shortened from futebol du salão, and partly from Spanish, shortened from fútbol sala or fútbol de salón .
